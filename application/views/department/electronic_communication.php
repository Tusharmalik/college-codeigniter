            <h2><b>About Us</b></h2>
            <br>

            The Department is headed by a highly experienced Head of Department Prof. (Dr.) A.N. Mishra. He has strong research background based on speech recognition, noise robust acoustic feature extraction, audio-visual speech recognition. He has rich experience of working in a team actively and collaboratively, and also strong motivation to advancement of speech technology research. The department comprises of faculty who have excelled in various fields of Electronics and Communication. The department conducts workshops, symposia and faculty development programs regularly for improving the teaching and learning quality. Our students are implementing their skills and the knowledge in the companies such as HP, Tech Mahindra, Mentor Graphics, Robotech Pvt. Ltd., HCL Technologies, TCS, IBM India, Genpact, etc as Telecom Engineer, Circuit Designer, Software Engineer, etc. <br><br>

    <p align="justify">
    <strong>Program Educational Objectives (PEOs)</strong> <br><br>
<ol>
<li><strong>Preparation:</strong><br> To prepare to excel in undergraduate program and succeed in industrial/technical profession through rigorous theoretical and practical education.</li>

<li><strong>Core Competence:</strong><bR> To provide students with a solid foundation in mathematics, science and electronics engineering fundamentals which is required to solve engineering problems in industry or society and which also help them in pursuing higher studies.</li>

<li><strong>Breadth:</strong> <bR>To train students with good scientific and engineering breadth so as to comprehend, analyze, design and create novel products and solutions for the real life problems.</li>

<li><strong>Professionalism:</strong> <bR>To inculcate in students professional and ethical attitude, effective communication skills, teamwork skills, multidisciplinary approach and an ability to relate engineering issues with broader social context.</li>

<li><strong>Learning Environment:</strong><br> To provide students with an academic environment thriving for excellence, leadership, written ethical codes and guidelines and the life-long learning needed for a successful professional carrier.</li>

<li><strong>Excellence in career:</strong> <br>To excel in their careers by being a part of success and growth of an organization with which they are associated.</li>
</ol><bR>
<strong>SCOPE OF THE DEPARTMENT</strong><Br>
<div align="justify">The graduates from this department have become professional engineers, employed both in core and software companies. They are well represented at core companies, such as Robert Bosch, Qualcomm, Aricent Group, Wipro and as well as smaller start-up companies. They have become successful software developers and managers in the leading software companies, such as Thought works, Infosys, Cognizant Technology Solutions, HCL Technologies, TCS, Igate etc. Many of our students pursue higher education to obtain MS, MBA and PhD degrees in foreign universities. Also, some of the graduates working in Public sector units, such as BSNL, National Thermal Power Corporation(NTPC) Ltd, DRDO, ISRO, BHEL, BEL etc.<br><br></div>
<strong>Department Vision:</strong><br>
To be a place where students & faculty find a highly motivating environment to study, carry out research & to work with their own hands.<br><bR>
<strong>Department Mission:</strong>
<ol>
<li>To make students employable in Industry.</li>
<li>To promote competitive academic programs and ambience supporting intellectual growth & skill acquisition.</li>
<li>To imbibe in them the quality of adapting themselves to the prevailing global environment successfully.</li>
</ol>
