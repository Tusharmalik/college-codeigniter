            <h2><b>About Us</b></h2>
            <br><div align="justify">
    <p align="justify">
   <p><h4><b>Vision</b></h4></p>
   <p>
To be recognized as a centre of excellence in academics and research in mechanical engineering, with state-of-the-art laboratories, good teaching infrastructure and qualified faculty to nurture and support scholars to become global players.</p><br>
<p><h4><b>Mission</b></h4></p>
<p><strong>M1:</strong> To equip our graduates with the knowledge and expertise with extra emphasis on technical and soft skills, having cross cultural adaptability, to contribute significantly to the industry and society as a whole. <br><br>
<strong>M2:</strong> To provide a platform where both faculty and students engage in scholarly research activities and also nurture their entrepreneurial and interpersonal skills.

   </p>
    </p>
    <p><h4><b>Aim</b></h4></p>
   <p>
The aim of mechanical department to give graduates the necessary skills but also to shape the thinking and team-working spirit to ensure the best prospects for successful careers and / or further study within the heavy demands of modern times. </p><br>
<p><h4><b>Scope</b></h4></p>
   <p>
To acquire the knowledge, Department develops suitable theoretical background studies with state of art laboratory and hand on skills training, and department infrastructure for applied technology research. By obtaining theoretical and applied scientific training, our graduates are equipped with the necessary knowledge and skills to meet the particularly demanding market in the best possible way.</p><br>

<p><h4><b>Programme Educational Objectives (PEOs)</b></h4></p>
   <p>
1. To provide excellent education in Mechanical Engineering to have all round development & expansion of students’ knowledge  in order to serve the global society. <br>
2. To facilitate the students with strong fundamentals and methods of simulation to work in design & analysis, fabrication & maintenance of mechanical systems. <br>
3. To provide opportunity to the students to expand their prospect beyond Mechanical Engineering and create novel products and solutions for the real life problems. <br>
4. To build a flexible and robust ambience that promotes originality and innovation among students & faculty, which encourage professional ethics and lifelong learning. <br>
5. To make the students attentive about the importance of multidisciplinary engineering approach, ethical attitude, team work and ability to relate engineering issues to the broader societal context. <br>
6. To introduce the students to the professionalism with effective soft skills and decision making ability.

</p><br>

