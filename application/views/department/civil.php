            <h2><b>About Us</b></h2>
            <br>
    <p align="justify">Civil engineering is the oldest and broadest engineering discipline among all the engineering fields. The field deals with the planning, designing, and construction of buildings and various other structures. From huge dams to sky high buildings, from suspension bridges to offshore drilling platforms, every physical concrete structure comes under civil engineering. Civil engineers are involved in monitoring and guiding as well as devising and designing the building of roads, girders, bridges, airports, tunnels, dams, water & sewage systems and other structures. <br><br>
The Department of Civil Engineering at KEC Ghaziabad is committed to academic excellence, research and development since its formation in the academic session 2014-15. The department is growing steadily with expert faculties in areas such as analysis and design of structures, hydraulics and hydrology, water resources, geotechnical engineering, construction technology and management, environment engineering, basic and advanced surveying.<br><br>
The vision of the department is to give an exposure to budding civil technocrats to various challenges in the profession.</strong>

