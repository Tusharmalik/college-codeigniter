            <h2><b>About Us</b></h2>
            <div align="justify">

    <p align="justify">
  <strong> AIM:</strong><br>
The department of Applied Sciences aims to provide a strong foundation in the fundamental sciences subjects of Engineering Mathematics, Physics, Chemistry, Environment Ecology and Professional Communication. Students of engineering are provided with the basic tools of analysis as well as the knowledge of the principles on which engineering is based.<Br><bR>

<strong>SCOPE:</strong><br>
Department of Applied Sciences is one of the indispensable departments of the Institute. It imparts excellent quality education in the area of Applied Sciences (Mathematics, Physics, and Chemistry) and English & Professional Communication skills for the future technocrats of all streams.
The department also strives to instill the scientific temper and the spirit of enquiry in students. It encourages the students to understand and therefore apply the physical laws to the development of their fields of study. The Department has well equipped laboratories for Physics, Chemistry and Professional Communication Skills.<br>
The department provides the students with a number of opportunities to develop their overall personality by participating in various cultural and co-curricular activities held throughout the year .The faculty members of the department take very active part in organizing these activities.
The department can boast of its highly qualified, experienced and dedicated faculty.<br><br>

<strong>VISION:</strong><Br>
Applied Sciences Department envisions as an academic workforce developer for the Technical Education Excellency. We instill high levels of discipline in students and strive to set global standards, making our students technologically superior and ethically strong, who in turn shall contribute to the advancement of society and mankind.<Br><Br>

<strong>MISSION:</strong><bR>
The mission of the Applied Sciences Department is to achieve, sustain and foster unmatched excellence in technical education. We pursue continuous development of infrastructure and enhance state-of-the art facilities to provide our students a technologically up-to-date and intellectually inspiring environment of learning and ensure high quality content by qualified faculty members who set the marks of excellence in their every endeavour.<Br><br>

<strong>PROGRAMME EDUCATIONAL OBJECTIVE:</strong><br><br>
<ol>
<li><strong>Knowledge Acquisition:</strong>
To understand the applications of Mathematics and Sciences in Engineering Technology and to train all the students for modern Engineering Applications.
</li>
<li><strong>Innovative Skills:</strong>
To enable our graduates in the professional practice of Engineering and to make them able to identify, formulate and implement applied knowledge in their technical innovations.
</li>
<li><strong>Social Contribution:</strong>
To motivate the students for social contribution and teach them professional ethics and moral responsibilities.
</li>
</ol><br>
<strong>DEPARTMENT / STUDENT’S ACHIEVEMENTS:</strong><br>
During the Academic Session 2015-16, 42 students scored more than 80% aggregate marks and one student scored 92.3 % aggregate marks, prospective university topper.
