            <h2><b>About Us</b></h2>



    <p align="justify">

    Established in 2004, the department of Electrical and Electronics Engineering has been playing a vital role in producing technologist in this core branch of Engineering. With technically enriched Machine and Power System Laboratory, the students of Electrical & Electronics branch are fortunate enough to study the basics of both the fields of Engineering. The department runs 8 semester Under Graduate course with electives in advanced power systems, renewable energy and Satellite communication technology. Besides the department also runs various skill development programs like Electric motor winding, software based designing and simulation for skill based development of the students. Considering the future of energy from alternative and renewable sources, the Department is striving for setting up a Center of Excellence in the field of Solar photovoltaic by pursuing Innovation and Creativity. The department have distinguished faculty with PhD degree from renowned University and pursuing for PhD. Students of EN department have performed exceptionally well in academics and brought laurels to the college by participating in various competitions in India and abroad. The department provides ample scope for research and skill development.<br><br>



<strong>Vision:</strong> <br>To be a department of high repute with admirable academic ambience

      where students became a global player in all respect. <br><bR>





<strong>Mission:</strong> <Br>To provide our students an undergraduate education of high quality

         supported with multifarious skills set and make them a good human

         being sensitive to the cause of society.











