            <h2><b>About Us</b></h2>
            <br>
<p align="justify">

<strong>Welcome to the department of Humanities at Krishna Engineering College.</strong><br><bR>
    <b>Aim</b><br>
    The Department of Humanities play a significant role in developing students into true professionals ready to take on the business dynamics with  great zeal and conviction. We aim to train future Technocrats/Engineering Managers through concerted efforts and careful nurturing. The department caters for academic curriculum as per University syllabus in B Tech 2nd yr, 3rd yr and 4th yr across all branches. <br><br>
The students are taught Industrial Psychology and Industrial Sociology with an objective to make them familiar with industrial set up and corresponding behavioral patterns. The knowledge of Human values & Professional ethics ensure their right understanding in order to identify & establish the ultimate goals of life. Engineering Economics and Industrial Management enable them correlate their projects with the financial components in an industrial setting. We also develop the students keen to be the entrepreneurs, by imparting an in depth knowledge about the Entrepreneurship and basic requirements in setting up a small scale industry.

<br><br>

    <b>Vision:</b><br>
   Empowering students from diverse backgrounds to become productive and ethical technocrats/ Engineering Managers.<br><br>
    <b>Mision:</b><br>
    The Department of Management Studies has created new <strong>TEMPO</strong> in technical and managerial education with sole mission of converting students into true professionals and is committed to excellence in integrated technology, experiential education,   professional enrichment,   career orientation and student’s advancement.<br>
    <br><b>Programme Educational Objectives:</b><br>
    <ol>
    <li>To provide student with an academic environment  par  excellence, leadership abilities, written ethical codes  &  guidelines.</li>
    <li>To provide students with a solid foundation in managerial concepts and  fundamentals required to solve engineering problems.</li>
    <li>To train students with good managerial skills so as to comprehend, analyze, design and create novel products and solutions for the real life problems.</li>
    <li>To inculcate in students professional and ethical attitude, effective communication skills, teamwork and group dynamics, multidisciplinary approach and an ability to relate engineering issues to broader social context.</li>

    </ol>

    </p>


