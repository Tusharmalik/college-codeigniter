    <p align="justify">Krishna Engineering College has established a Women Empowerment Cell in the college campus to empower girl students, to sensitize all issues related to women (students and faculties both) and to make the college campus a safe place for women students and faculty members.<br><br>
With a view to taking up women’s issues and problems, the cell aims at creating awareness of their rights and duties. It also provides a platform for women to share their experiences and views regarding their status in the society and to suggest ways to improve and empower themselves.<br><br>
Aiming at intellectual and social upliftment of the female students, the cell stands for facilitating women’s empowerment through guest lectures, seminars, awareness programmes and other welfare activities.
</p>

