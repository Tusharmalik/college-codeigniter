    <h4><b>About Library</b></h4>
    <p align="justify">
      The KEC Library serves as a place where students as well as faculty members can delve deeper into Their subjects and widen the horizons of their knowledge with the changes World.<br /><br />The KEC Library has a collection of more than 79194 books of Engineering, Applied Science Humanities, Management etc. It also has ASCE, Willey Blackwell,Springer Membership for online journals and DELNET membership for resource sharing.<br /><br />In order to help its students and faculty members to keep abreast with the changes happening in the world of technology ,the KEC Library also subscribes to 180 online Journals, periodicals and regional And national newspapers. A substantial collection of CD’s covering the latest programs. The college Also adopt a fully computerized RAMLIB software with bar coding system.<br /><br />The air conditioned KEC Library is spread across 1005sqm and has sufficient seating capacity. It is Equipped with a reference section , separate reading section for students and faculty members, Digital Section for Opec, Online Journals ,NPTEL facility and DELNET resource sharing.
